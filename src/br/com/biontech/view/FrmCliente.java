    private void txtCodigoKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtCodigoKeyPressed
        if (evt.getKeyCode() == java.awt.event.KeyEvent.VK_ENTER) {
            try {
                int id = Integer.parseInt(txtCodigo.getText());

                br.com.biontech.dao.ClienteDAO dao = new br.com.biontech.dao.ClienteDAO();
                br.com.biontech.model.Cliente obj = dao.buscarPorId(id);

                if (obj.getId() > 0) {
                    
                    txtNome.setText(obj.getNome());
                    txtCpf.setText(obj.getCpf());
                    txtTelefone.setText(obj.getTelefone());
                    txtEndereco.setText(obj.getEndereco());

                    txtNome.requestFocus(); 
                } else {
                    javax.swing.JOptionPane.showMessageDialog(null, "Cliente não encontrado!");
                }
            } catch (HeadlessException | NumberFormatException e) {
                javax.swing.JOptionPane.showMessageDialog(null, "Erro: Digite um número válido!");
            }
        }
    }//GEN-LAST:event_txtCodigoKeyPressed

