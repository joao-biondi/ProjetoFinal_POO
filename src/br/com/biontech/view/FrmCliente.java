    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        try {
            br.com.biontech.model.Cliente obj = new br.com.biontech.model.Cliente();

            obj.setNome(txtNome.getText());
            obj.setCpf(txtCpf.getText());
            obj.setTelefone(txtTelefone.getText());
            obj.setEndereco(txtEndereco.getText());

            br.com.biontech.dao.ClienteDAO dao = new br.com.biontech.dao.ClienteDAO();
            dao.cadastrarCliente(obj);

            txtNome.setText("");
            txtCpf.setText("");
            txtTelefone.setText("");
            txtEndereco.setText("");

        } catch (Exception e) {
            javax.swing.JOptionPane.showMessageDialog(null, "Erro: " + e);
        }
    }//GEN-LAST:event_btnSalvarActionPerformed

    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        try {
            br.com.biontech.model.Cliente obj = new br.com.biontech.model.Cliente();

            obj.setId(Integer.parseInt(txtCodigo.getText())); 

            br.com.biontech.dao.ClienteDAO dao = new br.com.biontech.dao.ClienteDAO();
            dao.excluirCliente(obj);

            txtCodigo.setText("");
            txtNome.setText("");
            txtCpf.setText("");
            txtTelefone.setText("");
            txtEndereco.setText("");

        } catch (NumberFormatException e) {
            javax.swing.JOptionPane.showMessageDialog(null, "Erro: Digite o Código do cliente para excluir!");
        }
    }//GEN-LAST:event_btnExcluirActionPerformed

    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        try {
            br.com.biontech.model.Cliente obj = new br.com.biontech.model.Cliente();

            obj.setNome(txtNome.getText());
            obj.setCpf(txtCpf.getText());
            obj.setTelefone(txtTelefone.getText());
            obj.setEndereco(txtEndereco.getText());

            obj.setId(Integer.parseInt(txtCodigo.getText())); 

            br.com.biontech.dao.ClienteDAO dao = new br.com.biontech.dao.ClienteDAO();
            dao.alterarCliente(obj);

            txtCodigo.setText("");
            txtNome.setText("");
            txtCpf.setText("");
            txtTelefone.setText("");
            txtEndereco.setText("");

        } catch (NumberFormatException e) {
            javax.swing.JOptionPane.showMessageDialog(null, "Erro: Selecione um cliente primeiro! " + e);
        }
    }//GEN-LAST:event_btnEditarActionPerformed

