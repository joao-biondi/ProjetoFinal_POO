    private void txtCodigoKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtCodigoKeyPressed
        if (evt.getKeyCode() == java.awt.event.KeyEvent.VK_ENTER) {
        try {
            int id = Integer.parseInt(txtCodigo.getText());
            br.com.biontech.dao.FornecedorDAO dao = new br.com.biontech.dao.FornecedorDAO();
            br.com.biontech.model.Fornecedor obj = dao.buscarPorId(id);
            
            if (obj.getId() > 0) {
                txtRazao.setText(obj.getRazaoSocial());
                txtCnpj.setText(obj.getCnpj());
                txtContato.setText(obj.getContato());
                txtRazao.requestFocus();
            } else {
                javax.swing.JOptionPane.showMessageDialog(null, "Fornecedor não encontrado!");
            }
        } catch (HeadlessException | NumberFormatException e) {
        }
    }
    }//GEN-LAST:event_txtCodigoKeyPressed

