    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        try {
            br.com.biontech.model.Fornecedor obj = new br.com.biontech.model.Fornecedor();

            obj.setRazaoSocial(txtRazao.getText());
            obj.setCnpj(txtCnpj.getText());
            obj.setContato(txtContato.getText());

            br.com.biontech.dao.FornecedorDAO dao = new br.com.biontech.dao.FornecedorDAO();
            dao.cadastrarFornecedor(obj);

            txtRazao.setText("");
            txtCnpj.setText("");
            txtContato.setText("");

        } catch (Exception e) {
            javax.swing.JOptionPane.showMessageDialog(null, "Erro: " + e);
        }
    }//GEN-LAST:event_btnSalvarActionPerformed

    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        try {
            br.com.biontech.model.Fornecedor obj = new br.com.biontech.model.Fornecedor();

            obj.setId(Integer.parseInt(txtCodigo.getText())); 

            br.com.biontech.dao.FornecedorDAO dao = new br.com.biontech.dao.FornecedorDAO();
            dao.excluirFornecedor(obj);

            txtRazao.setText("");
            txtCnpj.setText("");
            txtContato.setText("");
            txtCodigo.setText("");

        } catch (NumberFormatException e) {
            javax.swing.JOptionPane.showMessageDialog(null, "Digite o código para excluir!");
        }
    }//GEN-LAST:event_btnExcluirActionPerformed

    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        try {
            br.com.biontech.model.Fornecedor obj = new br.com.biontech.model.Fornecedor();

            obj.setRazaoSocial(txtRazao.getText());
            obj.setCnpj(txtCnpj.getText());
            obj.setContato(txtContato.getText());

            obj.setId(Integer.parseInt(txtCodigo.getText())); 

            br.com.biontech.dao.FornecedorDAO dao = new br.com.biontech.dao.FornecedorDAO();
            dao.alterarFornecedor(obj);

            txtRazao.setText("");
            txtCnpj.setText("");
            txtContato.setText("");
            txtCodigo.setText("");

        } catch (NumberFormatException e) {
            javax.swing.JOptionPane.showMessageDialog(null, "Selecione um fornecedor (digite o código) para editar!");
        }
    }//GEN-LAST:event_btnEditarActionPerformed

